use std::collections::HashMap;

fn main() {
    let input = _get_input();

    // ----------- Parse Input -----------

    let parsed = input
        .trim()
        .split("\n")
        .map(|s| s.split(" ").collect::<Vec<&str>>())
        .collect::<Vec<Vec<&str>>>();

    // ----------- Solve -----------

    let to_move = HashMap::from([("X", "A"), ("Y", "B"), ("Z", "C")]);
    let wins = HashMap::from([("A", "C"), ("B", "A"), ("C", "B")]);
    let loses = HashMap::from([("A", "B"), ("B", "C"), ("C", "A")]);
    let score = HashMap::<&str, i32>::from([("A", 1), ("B", 2), ("C", 3)]);
    let calc_score = |a: &str, b: &str| -> i32 { 
        score.get(b).unwrap() + (if b == a { 3 } else if &a == wins.get(b).unwrap() { 6 } else { 0 })
    };

    let p1 = parsed.iter().fold(0, |sum, arr| {
        let (a, b) = (arr[0], to_move.get(arr[1]).unwrap());
        return sum + calc_score(a, b);
    });

    let p2 = parsed.iter().fold(0, |sum, arr| {
        let (a, x) = (arr[0], arr[1]);
        let b = if x == "X" { 
            wins.get(a).unwrap() 
        } else if x == "Y" { 
            a 
        } else { 
            loses.get(a).unwrap() 
        };
        return sum + calc_score(a, b);
    });

    // ----------- Print -----------

    println!("Part 1: {}", p1);
    println!("Part 2: {}", p2);
}

